    public void iniciarModelo() {
        Profesor profe = new Profesor();
        String[] cabecera = {"ID", "Nombre", "Correo", "Edad"};
        moTabla.setColumnIdentifiers(cabecera);
        for (Profesor p : profe.listar()) {
            moTabla.addRow(new Object[]{p.getId(), p.getNombre(), p.getCorreo(), p.getEdad()});
        }
        tblProfesores.setModel(moTabla);

        Curso curso = new Curso();
        for (Curso c : curso.listar()) {
            moListaCurso.addElement(c.getId() + "-" + c.getNombre());
        }
        lstCursos.setModel(moListaCurso);

        String[] cabeceraCurso = {"ID", "Nombre", "Celular","Edad"};
        moTablaCurso.setColumnIdentifiers(cabeceraCurso);
//        for (Curso c : curso.listar()) {
//            moTablaCurso.addRow(new Object[]{c.getId(), c.getNombre(), c.getCapacidad()});
//        }
        tblEstudiantesCurso.setModel(moTablaCurso);
    }

