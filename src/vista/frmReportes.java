    private void lstCursosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstCursosMouseClicked
        Curso curso = new Curso();
        int id = Integer.parseInt(lstCursos.getSelectedValue().split("-")[0]);
        moTablaCurso.setRowCount(0);
        for (Estudiantes alumno : curso.listarEstudiantes(id)) {
            moTablaCurso.addRow(new Object[]{alumno.getId(), alumno.getNombre(), alumno.getCelular(), alumno.getEdad()});
        }
    }//GEN-LAST:event_lstCursosMouseClicked

